# 資料型態
# 數字
123
1.2
# 字串
"Hello World"
# 布林值
True
False
# 有順序、可動的列表 List
[1, 2, 3]
["Hello", "World"]
# 有順序、不可動的列表 Tuple
(1, 2, 3)
("Hello", "World")
# 集合 Set
{3, 4, 5}
{"Hello", "World"}
# 字典 Dictionary
{"apple": "蘋果", "book": "書"}
# 變數
# 變數名稱 = 資料
x = 1
print(x)
print([1, 2])
x = True
print(x)
x = {1, 2, 3, 4}
print(x)
